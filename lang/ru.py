_PUNCT = re.compile(r"[^\w\s]", re.UNICODE)

def tokenize(text: str) -> list[str]:
    """Убирает пунктуацию, lowercase, возвращает список токенов."""
    cleaned = re.sub(r"\s+", " ", _PUNCT.sub(" ", text.lower())).strip()
    return cleaned.split()


