# Минимальная длина слова для стемминга.
# Слова короче — возвращаем как есть.
_STEM_MIN_LEN = 6


def _strip(word: str, suffixes: tuple[str, ...], min_stem: int = 3) -> tuple[str, bool]:
    for suf in suffixes:
        if word.endswith(suf) and len(word) - len(suf) >= min_stem:
            return word[: len(word) - len(suf)], True
    return word, False


def stem(word: str) -> str:
    """
    Snowball-стеммер для русского.
    Слова короче _STEM_MIN_LEN символов возвращаются без изменений.

    Примеры:
        компьютеры  → компьютер   (10 симв → стеммируем)
        атом        → атом        (4 симв  → не трогаем)
        физик       → физик       (5 симв  → не трогаем)
        квантового  → квантов     (9 симв  → стеммируем)
        расскажи    → рассказ     (8 симв  → стеммируем)
    """
    w = word.lower().strip()

    # Короткие слова не стеммируем — они и так в нормальной форме
    if len(w) < _STEM_MIN_LEN:
        return w

    w, _ = _strip(w, _REFLEXIVE)

    w2, ok = _strip(w, _PERFECTIVE_GERUND)
    if ok:
        w = w2
    else:
        _, adj_ok  = _strip(w, _ADJECTIVE)
        w3, prt_ok = _strip(w, _PARTICIPLE)
        if adj_ok and prt_ok:
            w = w3
        elif not prt_ok:
            w, verb_ok = _strip(w, _VERB)
            if not verb_ok:
                w, _ = _strip(w, _NOUN)
        else:
            w = w3

    w, _ = _strip(w, _DERIVATIONAL)
    w, _ = _strip(w, _SUPERLATIVE)

    if w.endswith("ь"):
        w = w[:-1]
    if w.endswith("нн"):
        w = w[:-1]

    # Защита: если стем получился короче 3 — возвращаем оригинал
    return w if len(w) >= 3 else word.lower()


